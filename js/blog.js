function formatBlogDate(dateString) {
  if (!dateString) {
    return '';
  }

  return new Date(dateString).toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric'
  });
}

function getFeaturedPosts() {
  // Featured posts for homepage: 2 newest (last in array) + 1 rotating from the rest
  const newest = blogPosts.slice(-2).reverse();
  const older = blogPosts.slice(0, -2);
  const daysSinceEpoch = Math.floor(Date.now() / (1000 * 60 * 60 * 24));
  const rotatingIndex = daysSinceEpoch % older.length;
  return [...newest, older[rotatingIndex]];
}

function getAllPosts() {
  // All posts for blog page, newest first
  return [...blogPosts].reverse();
}

function getBlogHubArchivePosts() {
  return getAllPosts().filter(post => !blogHubFeaturedSlugs.has(post.slug));
}

function getBlogPostHref(postOrSlug) {
  const slug = typeof postOrSlug === 'string' ? postOrSlug : postOrSlug?.slug;
  if (!slug) {
    return 'blog.html';
  }

  return staticBlogPostPaths[slug] || `blog-post.html?slug=${slug}`;
}

function isStaticBlogPost(postOrSlug) {
  const slug = typeof postOrSlug === 'string' ? postOrSlug : postOrSlug?.slug;
  return !!(slug && staticBlogPostPaths[slug]);
}

function displayBlogPosts() {
  const blogGrid = document.querySelector('[data-blog-feed="archive"]') || document.querySelector('.blog-grid');
  if (!blogGrid) return;

  blogGrid.innerHTML = '';

  // Blog page shows all posts; homepage shows 3 featured
  const isBlogPage = !!document.querySelector('.blog-section h1');
  const posts = isBlogPage ? getBlogHubArchivePosts() : getFeaturedPosts();

  posts.forEach(post => {
    const journey = postJourneys[post.slug];
    const dateLabel = formatBlogDate(post.date);
    const article = document.createElement('article');
    article.className = 'blog-card';

    article.innerHTML = `
      <img src="${post.image_url}" alt="${post.title}" class="blog-image" loading="lazy">
      <div class="blog-content">
        <span class="blog-category">${post.category}</span>
        <h2>${post.title}</h2>
        <p>${post.excerpt}</p>
        <div class="blog-meta">
          <span>${dateLabel}</span>
          <span>${journey ? journey.moneyPage.shortLabel : 'P Z M Mobile & Computers Dubai'}</span>
        </div>
        <a href="${getBlogPostHref(post)}" class="read-more">Read More</a>
      </div>
    `;

    blogGrid.appendChild(article);
  });
}

// Initialize blog posts when DOM is ready
document.addEventListener('DOMContentLoaded', displayBlogPosts);

export { blogPosts, postJourneys, formatBlogDate, staticBlogPostPaths, getBlogPostHref, isStaticBlogPost };
